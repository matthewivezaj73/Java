//Creating a public class.
public class ArrayFun {
    //Creating main method.
    public static void main(String[] args)
    {
        //Creating an array reference.
        int[] myArray;
        //Initializing the array
        myArray = new int[5];
        //Assigning a value to each index in the array.
        myArray[0] = 10;
        myArray[1] = 22;
        myArray[2] = 5;
        myArray[3] = 17;
        myArray[4] = 20;
    }
}
